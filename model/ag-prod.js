class Productos {
    
    id;

    imagen;

    nombre;

    marca;

    precio;

    cantidad;

    constructor(id, imagen, nombre, marca, precio, cantidad) {

        this.id = id;

        this.imagen = imagen;

        this.nombre = nombre;

        this.marca = marca;

        this.precio = precio;

        this.cantidad = cantidad;

    }

}
